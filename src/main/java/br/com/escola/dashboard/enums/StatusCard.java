package br.com.escola.dashboard.enums;

/**
 * 🔹 StatusCard
 *
 * Esse enum define os possíveis status de um card/evento no sistema.
 *
 * PENDENTE      -> ainda não começou
 * EM_ANDAMENTO -> está sendo realizado / preparado
 * CONCLUIDO    -> já foi finalizado
 */
public enum StatusCard {
    PENDENTE,
    EM_ANDAMENTO,
    CONCLUIDO
}